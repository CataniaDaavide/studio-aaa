// === Main table component ===
function DataTable({ data, columns }) {
  const [sorting, setSorting] = React.useState([]);
  const [filter, setFilter] = React.useState("");

  const table = useReactTable({
    data,
    columns,
    state: {
      sorting,
      globalFilter: filter,
    },
    onSortingChange: setSorting,
    onGlobalFilterChange: setFilter,
    getCoreRowModel: getCoreRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    getSortedRowModel: getSortedRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
  });

  return (
    <Card className="w-full p-0 gap-0">
      <div className="flex items-center justify-between gap-3 border-b p-6">
        {/* Search bar */}
        <Input
          placeholder="Search..."
          value={filter ?? ""}
          onChange={(e) => setFilter(e.target.value)}
          className="max-w-sm"
        />
        <div className="flex gap-3">
          <Button variant={"primary"}>Inserisci</Button>
          <Button variant={"outline"}>Esporta</Button>
        </div>
      </div>
      <div className="flex flex-col gap-3 p-6 border-b">
        <div className="grid grid-cols-1 md:grid-cols-4 gap-6">
          {/* filtri */}
          <div className="grid gap-1">
            <Label>Nome utente</Label>
            <Input />
          </div>
          <div className="grid gap-1">
            <Label>Nome</Label>
            <Input />
          </div>
          <div className="grid gap-1">
            <Label>Ruolo</Label>
            <Input />
          </div>
          <div className="grid gap-1">
            <Label>Stato</Label>
            <Input />
          </div>
        </div>
        <div className="flex flex-wrap gap-3">
          <Button variant="primary">Cerca</Button>
          <Button variant="outline">Annulla</Button>
        </div>
      </div>

      {/* Table */}
      <CardContent className="overflow-hidden rounded-md border-b px-6 py-3">
        <Table>
          <TableHeader>
            {table.getHeaderGroups().map((headerGroup) => (
              <TableRow key={headerGroup.id}>
                {headerGroup.headers.map((header) => (
                  <TableHead key={header.id}>
                    {flexRender(
                      header.column.columnDef.header,
                      header.getContext()
                    )}
                  </TableHead>
                ))}
              </TableRow>
            ))}
          </TableHeader>

          <TableBody>
            {table.getRowModel().rows.length ? (
              table.getRowModel().rows.map((row) => (
                <TableRow key={row.id}>
                  {row.getVisibleCells().map((cell) => (
                    <TableCell key={cell.id}>
                      {flexRender(
                        cell.column.columnDef.cell,
                        cell.getContext()
                      )}
                    </TableCell>
                  ))}
                </TableRow>
              ))
            ) : (
              <TableRow>
                <TableCell
                  colSpan={columns.length}
                  className="text-center h-24"
                >
                  No results.
                </TableCell>
              </TableRow>
            )}
          </TableBody>
        </Table>
      </CardContent>

      {/* Pagination */}
      <div className="flex items-center justify-end space-x-2 gap-3 p-6">
        <Button
          variant="outline"
          size="lg"
          onClick={() => table.previousPage()}
          disabled={!table.getCanPreviousPage()}
        >
          Previous
        </Button>
        <Button
          variant="outline"
          size="lg"
          onClick={() => table.nextPage()}
          disabled={!table.getCanNextPage()}
        >
          Next
        </Button>
      </div>
    </Card>
  );
}

// === Page ===
export default function UsersPage() {
  return (
    <div className="w-full flex justify-center p-4">
      <DataTable data={data} columns={columns} />
    </div>
  );
}
